'use client';

import React, { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { useResultsStore, useUploadStore } from '../../store';
import { PageContainer } from '../../components/common/PageContainer';
import { CRMResultTable } from '../../components/results/CRMResultTable';
import { SkippedRecordsTable } from '../../components/results/SkippedRecordsTable';
import { Card, CardContent } from '../../components/ui/card';
import { Button } from '../../components/ui/button';
import { copyToClipboard, downloadJson, downloadCsv } from '../../utils/exportHelpers';
import {
  CheckCircle2,
  AlertTriangle,
  XCircle,
  Copy,
  Download,
  Database,
  ArrowLeft,
  Share2,
} from 'lucide-react';
import { formatPercentage, formatNumber } from '../../utils';
import { cn } from '../../lib/utils';

export default function ResultsPage() {
  const router = useRouter();
  const { fileMeta } = useUploadStore();
  const { crmRecords, skippedRecords, stats } = useResultsStore();
  const [activeTab, setActiveTab] = useState<'crm' | 'skipped'>('crm');

  // Redirect to import if no results are loaded and no active file metadata
  useEffect(() => {
    if (!stats && !fileMeta) {
      router.replace('/import');
    }
  }, [stats, fileMeta, router]);

  // Fallback statistics if page is viewed without an active upload
  const finalStats = stats || {
    imported: crmRecords.length || 0,
    skipped: skippedRecords.length || 0,
    failed: 0,
    warnings: 0,
    processingTime: 0,
    averageConfidence: 0,
    recordsPerSecond: 0,
    totalCost: 0,
  };

  const successRate = (finalStats.imported + finalStats.skipped) > 0
    ? (finalStats.imported / (finalStats.imported + finalStats.skipped)) * 100
    : 0;

  return (
    <PageContainer>
      {/* Top Header Actions bar */}
      <div className="flex flex-col sm:flex-row sm:items-center justify-between gap-4 select-none">
        <div>
          <h1 className="text-2xl md:text-3xl font-extrabold tracking-tight">Import Results</h1>
          <p className="text-sm md:text-base text-muted-foreground">
            Review successfully ingested lead profiles and warnings generated by the AI parser.
          </p>
        </div>

        <div className="flex flex-wrap items-center gap-2 w-full sm:w-auto">
          <Button
            variant="outline"
            onClick={() => copyToClipboard(crmRecords)}
            className="flex-1 sm:flex-none border-border"
            disabled={crmRecords.length === 0}
          >
            <Copy className="h-4 w-4 mr-2" />
            Copy JSON
          </Button>
          <div className="flex items-center gap-2 flex-1 sm:flex-none">
            <Button
              variant="outline"
              onClick={() => downloadJson(crmRecords)}
              className="flex-1 sm:flex-none border-border"
              disabled={crmRecords.length === 0}
            >
              <Share2 className="h-4 w-4 mr-2" />
              JSON
            </Button>
            <Button
              onClick={() => downloadCsv(crmRecords)}
              className="flex-1 sm:flex-none shadow-sm"
              disabled={crmRecords.length === 0}
            >
              <Download className="h-4 w-4 mr-2" />
              Export CSV
            </Button>
          </div>
        </div>
      </div>

      {/* Overview Statistics Grid */}
      <div className="grid gap-4 grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 select-none">
        <div className="p-5 bg-card border border-border shadow-sm rounded-2xl flex items-center gap-4">
          <div className="h-11 w-11 rounded-xl bg-success/10 text-success flex items-center justify-center shadow-inner shrink-0">
            <CheckCircle2 className="h-5 w-5" />
          </div>
          <div>
            <span className="text-[10px] font-bold text-muted-foreground uppercase tracking-wider">Imported</span>
            <p className="text-2xl font-extrabold tracking-tight mt-0.5">{formatNumber(finalStats.imported)}</p>
          </div>
        </div>

        <div className="p-5 bg-card border border-border shadow-sm rounded-2xl flex items-center gap-4">
          <div className="h-11 w-11 rounded-xl bg-destructive/10 text-destructive flex items-center justify-center shadow-inner shrink-0">
            <XCircle className="h-5 w-5" />
          </div>
          <div>
            <span className="text-[10px] font-bold text-muted-foreground uppercase tracking-wider">Skipped</span>
            <p className="text-2xl font-extrabold tracking-tight mt-0.5">{formatNumber(finalStats.skipped)}</p>
          </div>
        </div>

        <div className="p-5 bg-card border border-border shadow-sm rounded-2xl flex items-center gap-4">
          <div className="h-11 w-11 rounded-xl bg-warning/10 text-warning flex items-center justify-center shadow-inner shrink-0">
            <AlertTriangle className="h-5 w-5" />
          </div>
          <div>
            <span className="text-[10px] font-bold text-muted-foreground uppercase tracking-wider">Warnings</span>
            <p className="text-2xl font-extrabold tracking-tight mt-0.5">{formatNumber(finalStats.warnings)}</p>
          </div>
        </div>

        <div className="p-5 bg-card border border-border shadow-sm rounded-2xl flex items-center gap-4">
          <div className="h-11 w-11 rounded-xl bg-primary/10 text-primary flex items-center justify-center shadow-inner shrink-0">
            <Database className="h-5 w-5" />
          </div>
          <div>
            <span className="text-[10px] font-bold text-muted-foreground uppercase tracking-wider">AI Certainty</span>
            <p className="text-2xl font-extrabold tracking-tight mt-0.5">{formatPercentage(finalStats.averageConfidence)}</p>
          </div>
        </div>
      </div>

      {/* Grid containing Performance summary */}
      <div className="grid grid-cols-2 sm:grid-cols-4 gap-4 bg-secondary p-4 rounded-2xl border border-border select-none text-xs">
        <div className="flex flex-col">
          <span className="text-muted-foreground font-medium">Pipeline Duration</span>
          <span className="text-sm font-bold text-foreground mt-0.5">
            {finalStats.processingTime} seconds
          </span>
        </div>
        <div className="flex flex-col">
          <span className="text-muted-foreground font-medium">Success Rate</span>
          <span className="text-sm font-bold text-foreground mt-0.5">
            {successRate.toFixed(1)}%
          </span>
        </div>
        <div className="flex flex-col">
          <span className="text-muted-foreground font-medium">Average Speed</span>
          <span className="text-sm font-bold text-foreground mt-0.5 font-mono">
            {finalStats.recordsPerSecond.toFixed(1)} rows/sec
          </span>
        </div>
        <div className="flex flex-col">
          <span className="text-muted-foreground font-medium">Total Billed Cost</span>
          <span className="text-sm font-bold text-foreground mt-0.5 font-mono">
            ₹{finalStats.totalCost.toFixed(2)}
          </span>
        </div>
      </div>

      {/* Tabs Header */}
      <div className="flex flex-col gap-4">
        <div className="flex border-b border-border select-none">
          <button
            onClick={() => setActiveTab('crm')}
            className={cn(
              'px-5 py-3 text-xs md:text-sm font-semibold border-b-2 -mb-[2px] transition-colors',
              activeTab === 'crm'
                ? 'border-primary text-primary'
                : 'border-transparent text-muted-foreground hover:text-foreground'
            )}
          >
            CRM Records ({crmRecords.length})
          </button>
          <button
            onClick={() => setActiveTab('skipped')}
            className={cn(
              'px-5 py-3 text-xs md:text-sm font-semibold border-b-2 -mb-[2px] transition-colors',
              activeTab === 'skipped'
                ? 'border-primary text-primary'
                : 'border-transparent text-muted-foreground hover:text-foreground'
            )}
          >
            Skipped / Failures ({skippedRecords.length})
          </button>
        </div>

        {/* Tab content screens */}
        <div className="w-full">
          {activeTab === 'crm' ? (
            <CRMResultTable />
          ) : (
            <SkippedRecordsTable />
          )}
        </div>
      </div>
    </PageContainer>
  );
}
